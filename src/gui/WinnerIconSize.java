package gui;

public class WinnerIconSize {
	private int width;
	private int height;
	private String name;
	
	public WinnerIconSize(int width, int height, String name) {
		this.width = width;
		this.height = height;
		this.name = name;
	}
	
	public int getWidth() {
		return width;
	}
	
	public int getHeight() {
		return height;
	}
	
	public String getName() {
		return name;
	}
	

}
